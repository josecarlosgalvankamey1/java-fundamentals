public class IntroToArrays {
    public static void main(String[] args) {
        String[] kingdoms = { "Mercia", "Wessex", "Northumbria", "East Anglia" };

        System.out.println("Kingdom 0: " + kingdoms[0]);
        System.out.println("Kingdom 1: " + kingdoms[1]);
        System.out.println("Kingdom 2: " + kingdoms[2]);
        System.out.println("Kingdom 3: " + kingdoms[3]);
    }
}